// 2: Арифметические операции с переменными типа double

#include <iostream>
using namespace std;

int main() {
    double a = 3.14, b = 2.71;
    cout << "Сложение: " << a + b << endl;
    cout << "Вычитание: " << a - b << endl;
    cout << "Умножение: " << a * b << endl;
    cout << "Деление: " << a / b << endl;
    return 0;
}